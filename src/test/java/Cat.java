public class Cat {
    //--------------------------ATTRIBUTES------------------------------------------------------
    private double weight;
    private int age;
    private String color;
    private boolean isCatABoy;
    private double height;

    //---------------------------GETTERS/SETTERS-------------------------------------------------

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public boolean isCatABoy() {
        return isCatABoy;
    }

    public void setCatABoy(boolean catABoy) {
        isCatABoy = catABoy;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
